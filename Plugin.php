        $app->config['mailer.templates']['rcv_send_registration_update'] = [
            'title' => i::__("[Cultura Viva] Atualização enviada com sucesso!"),
            'template' => 'rcv_send_registration_update.html'
        ];


        $opportunity_ids = $this->config['rcvOpportunityIds'];
        $app->hook('ApiQuery(Opportunity).params', function (&$api_params) use($app, $opportunity_ids){
            if($request = $app->request){
                $referer = $request->getReferer();
                $disabled_referers = [
                    $app->createUrl('site', 'index'),
                    $app->createUrl('search', 'opportunities'),
                ];
                if($app->request->isAjax() && $referer && in_array($referer[0], $disabled_referers)){
                    if(isset($api_params['id'])){
                        $api_params['id'] = API::AND($api_params['id'], API::NOT_IN($opportunity_ids));
                    } else {
                        $api_params['id'] = API::NOT_IN($opportunity_ids);
                    }
                    $api_params['status'] = API::GT(0);
                }
            }
            
        });

        $app->hook('registrationFieldTypes.saveToEntity', function($entity_field, $value) {
            /** @var Entities $this */
            if($entity_field === "rcv_links_coletivo") {
                $this->rcv_links_coletivo = json_encode($value);
                $this->save(true);
            }

            if($entity_field === "rcv_sede_realizaAtividades_outros_lista") {
                $this->rcv_sede_realizaAtividades_outros_lista = json_encode($value);
                $this->save(true);
            }
        });

        $app->hook('registrationFieldTypes.fetchFromEntity', function($entity_field, $value) {
            if($value && $entity_field === "rcv_links_coletivo") {
                $value = json_decode($value);
            }

            if($value && $entity_field === "rcv_sede_realizaAtividades_outros_lista") {
                $value = json_decode($value);
            }
        });

        $app->hook('can(Agent.modify)', function($user, &$can) use ($app, $self) {
            if (($app->subsite && $app->subsite->id != $self->config['rcv.subsiteId'] && $this->rcv_registration) && !$app->user->is('admin')) {
                $can = false;
            }
        });

        
        $app->hook('GET(<<registration|opportunity>>.<<*>>):before', function() use($app, $self) {
            if($app->subsite && $app->subsite->id == $self->config['rcv.subsiteId']) {
                return;
            }

            $entity = $this->requestedEntity;
            $must_redirect = false;

            if($entity instanceof Registration && in_array($entity->opportunity->id, $self->config['rcvOpportunityIds'])) {
                $must_redirect = true;
            } else if ($entity instanceof Opportunity && in_array($entity->id, $self->config['rcvOpportunityIds'])) {
                $must_redirect = true;
            }

            if($must_redirect) {
                $subsite = $app->repo('Subsite')->find($self->config['rcv.subsiteId']);
                $data = $this->data;
                if(isset($data['id'])) {
                    $data[0] = $data['id'];
                    unset($data['id']);
                }
    
                $app->subsite = $subsite;
                $url = $app->createUrl($this->id, $this->action, $data);
                $app->redirect($url);
                die;
            }
        });

        $function = function () use($self) {
            $public_columns = $self->config['rcv.publicSpreadsheetColumns'];
            if($this->subsite && $this->subsite->id == $self->config['rcv.subsiteId']) {
                $metadata = $this->getRegisteredMetadata('MapasCulturais\Entities\Agent');
    
                foreach($public_columns as $field){
                    if(in_array($field, array_keys($metadata))) {
                        $metadata[$field]->private = function() {
                            return isset($this->type) && $this->type->id == 1;
                        };
                    }
                }
            }
        };

        if(php_sapi_name() == "cli") {
            $app->hook("app.executeJob:before", $function);
        } else {
            $app->hook("app.register:after", $function);
        }
    }

